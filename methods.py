def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma(v1, v2):
    soma = v1 + v2
    return soma

def subtracao(v1, v2):
    subtracao = v1 - v2
    return subtracao

def multiplicacao(v1, v2):
    multiplicacao = v1 * v2
    return multiplicacao
